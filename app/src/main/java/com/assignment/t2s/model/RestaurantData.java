public class RestaurantData {
    String restName, restAddress, restLocation, restPhoneNo, restTimings;

    public String getRestName() {
        return restName;
    }

    public void setRestName(String restName) {
        this.restName = restName;
    }

    public String getRestAddress() {
        return restAddress;
    }

    public void setRestAddress(String restAddress) {
        this.restAddress = restAddress;
    }

    public String getRestLocation() {
        return restLocation;
    }

    public void setRestLocation(String restLocation) {
        this.restLocation = restLocation;
    }

    public String getRestPhoneNo() {
        return restPhoneNo;
    }

    public void setRestPhoneNo(String restPhoneNo) {
        this.restPhoneNo = restPhoneNo;
    }

    public String getRestTimings() {
        return restTimings;
    }

    public void setRestTimings(String restTimings) {
        this.restTimings = restTimings;
    }
}
